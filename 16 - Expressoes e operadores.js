/** EXPRESSÕES E OPERADORES */

// Operadores de atribuição

//(=) - Atribui valor a variável
let a = 1; //  Atribui o valor 1 para a variável 'a';

// (=+) - atribuição e adição
let b = 1;
b += 2; // Soma mais '2' a variável b;
console.log(b);// Saída: 3

// (-=) - Atribuição e subtração
let c = 2;
c += 1; // Subtrai 1 da  a variável b;
console.log(c);// Saída: 1

// (*=) - Atribuição e multiplicação
let d = 2;
d *= 2; // Atribui e multiplica;
console.log(d);// Saída: 4

// (/=) - Atribuição e divisão
let e = 2;
e /= 2; // Atribui e divide;
console.log(e);// Saída: 1

//---------------------------------------------------------
// OPERADORES DE COMPARAÇÃO

// (==) - igualdade, verifica se dois valores são iguais. 
// NÃO CONSIDERA SE OS TIPOS DE DADOS SÃO IGUAIS.
console.log(10 == 10); // true
console.log("5" == 5); // true

// (===) - igualdade estrita, verifica se valores e tipos são iguais.
console.log( 2 === 2); // true
console.log(2 === "2"); // false

// (!=) - Diferença - Verifica se dois valores são diferentes.
console.log(5 != 3); // true
console.log("5" != 5); // false - Não compara o tipo de dados.

// (!==) -  diferença estrita - Compara valores e tipos de dados.
console.log(5 !== 3); // true
console.log("5" !== 5); // true

// (>) e (<) - Compara se o valor é maior ou menor.
console.log(2 > 1);// true
console.log(5 < 3); // false

// (>=) e (<=) - Maior e menor igual
console.log(5 >= 5); // true
console.log(5 <= 3); // false




