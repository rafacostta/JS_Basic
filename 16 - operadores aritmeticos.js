// OPERADORES ARITMÉTICOS

// (+) - Adição
let a = 2 + 2;
console.log(a); // 4

// (-) - Subtração
var b = 10 - 2;
console.log(b); // 8

// (*) - Multiplicação
let c = 2 * 2;
console.log(c); // 4

// (/) - Divisão
let d = 10 / 2;
console.log(d); // 5

// (%) - Módulo - calcula o resto da divisão entre dois valores.
let e = 10 % 2; 
console.log(e); // Resultado 0
let f = 7 % 3;
console.log(f);  // Resultado 1

// (++) - Incremento, soma um ao valor de uma variável.
let g = 1;
g++;
console.log("Incrementa + 1 : " + g); // 2

// (--) - Decremento, Subtrai 1 da variável.
let h = 10;
h--;
console.log("Decremento : " + h); //9