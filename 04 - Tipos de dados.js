/** TIPOS de DADOS */

/**
 * 1 - Number - Inteiros e ponto flutuante.
 * 2 - String - Sequência de caracteres.
 * 3 - Boolean - True ou false.
 * 4 - Undefined - Representa valor indefinido, normalmente atribuido automaticamente para variáveis não 
 * inicializadas.
 * Null - Ausência intencional de qualquer valor.
 * 5 - Object - Estrutura de dados que pode armazenar uma coleção de dados e funções;
 * 6 - Array - Objeto especial para armazenar lista ordenadas.
 * 7 - Function - Objetos de função, usadas para definir blocos de código.
 * 8 - Symbol - Valor primitivo e único, usado como identificador de propriedades de objeto.
 * 9 - BigInt - Dado numérico que representa de precisão arbritária.
 * 
 */